package exercise;

public class Point {

    private int x;
    private int y;

    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int getX) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setGetY(int getY) {
        this.y = y;
    }
}